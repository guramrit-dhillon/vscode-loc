#!/usr/bin/env node
/**
 * Build VS Code i18n JSON files from translations.xlsx.
 *
 * Usage:
 *   node scripts/build-translations.mjs            # Build all language packs
 *   node scripts/build-translations.mjs --validate # Show coverage stats only
 *
 * Reads:  translations.xlsx
 * Writes: packages/<lang>/translations/main.i18n.json  (one per output language)
 *
 * Rows with an empty translation column are skipped for that language —
 * VS Code falls back to its built-in English string for any missing key.
 */

import { writeFileSync, mkdirSync, existsSync } from 'node:fs';
import { resolve, dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import { readXLSX, OUTPUT_LANGS } from './xlsx-utils.mjs';

const __dirname  = dirname(fileURLToPath(import.meta.url));
const ROOT       = resolve(__dirname, '..');
const REFERENCES = resolve(ROOT, 'references');
const XLSX_PATH  = join(REFERENCES, 'translations.xlsx');

const HEADER = [
  '--------------------------------------------------------------------------------------------',
  'Copyright (c) 2026 guramrit-dhillon. All rights reserved.',
  'Licensed under the MIT License. See License.txt in the project root for license information.',
  '--------------------------------------------------------------------------------------------',
  'Do not edit this file directly. Edit translations.xlsx and run: npm run translations:build',
];

const IS_VALIDATE = process.argv.includes('--validate');

// ─── Build ────────────────────────────────────────────────────────────────────

function buildI18nJson(rows, column) {
  /** @type {Record<string, Record<string, string>>} */
  const contents = {};
  for (const row of rows) {
    const { module: mod, key } = row;
    const value = row[column];
    if (!mod || !key || !value) continue;
    if (!contents[mod]) contents[mod] = {};
    contents[mod][key] = value;
  }
  return { '': HEADER, version: '1.0.0', contents };
}

// ─── Main ─────────────────────────────────────────────────────────────────────

function main() {
  if (!existsSync(XLSX_PATH)) {
    console.error(`translations.xlsx not found at ${XLSX_PATH}`);
    process.exit(1);
  }

  console.log(`Reading ${XLSX_PATH}...`);
  const rows = readXLSX(XLSX_PATH);
  const validRows = rows.filter((r) => r.module && r.key);
  console.log(`  ${validRows.length} valid rows (of ${rows.length} total)`);

  // Coverage report
  console.log('');
  for (const { column, label } of OUTPUT_LANGS) {
    const count = validRows.filter((r) => r[column]).length;
    const pct   = validRows.length > 0 ? ((count / validRows.length) * 100).toFixed(1) : '0.0';
    console.log(`  ${label.padEnd(22)}: ${count.toString().padStart(6)} / ${validRows.length}  (${pct}%)`);
  }
  console.log('');

  if (IS_VALIDATE) return;

  // Write JSON for each output language
  for (const { column } of OUTPUT_LANGS) {
    const i18n    = buildI18nJson(validRows, column);
    const outDir  = join(ROOT, 'packages', column, 'translations');
    const outPath = join(outDir, 'main.i18n.json');

    mkdirSync(outDir, { recursive: true });
    writeFileSync(outPath, JSON.stringify(i18n, null, 2) + '\n', 'utf-8');

    const mods  = Object.keys(i18n.contents).length;
    const strs  = Object.values(i18n.contents).reduce((s, m) => s + Object.keys(m).length, 0);
    console.log(`Written ${outPath}  (${mods} modules, ${strs} strings)`);
  }

  console.log('\nDone. Run `npm run build` to rebuild the extensions.');
}

main();
